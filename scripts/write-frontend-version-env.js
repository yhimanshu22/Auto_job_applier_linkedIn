#!/usr/bin/env node
/** Write NEXT_PUBLIC_DESKTOP_VERSION for the frontend build from repo VERSION. */
const fs = require("fs");
const path = require("path");
const { getRepoVersion, REPO_ROOT } = require("./read-version");

const envPath = path.join(REPO_ROOT, "frontend", ".env.production.local");
const version = getRepoVersion();
const contents = `# Generated by scripts/write-frontend-version-env.js — do not edit.\nNEXT_PUBLIC_DESKTOP_VERSION=${version}\n`;

if (fs.existsSync(envPath) && fs.readFileSync(envPath, "utf8") === contents) {
  console.log(`frontend/.env.production.local already at ${version}`);
} else {
  fs.writeFileSync(envPath, contents);
  console.log(`Wrote NEXT_PUBLIC_DESKTOP_VERSION=${version} to frontend/.env.production.local`);
}
